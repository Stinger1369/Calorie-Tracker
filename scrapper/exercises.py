# Fonction pour extraire les données d'une page d'exercice
def scrape_exercise_page(url):
    page = requests.get(url)
    soup = BeautifulSoup(page.content, 'html.parser')

    # Extraction du titre
    title = soup.find('h1', class_='entry-title').get_text() if soup.find('h1', class_='entry-title') else 'N/A'

    # Extraction de la description
    description = soup.find('p', class_='has-drop-cap').get_text() if soup.find('p', class_='has-drop-cap') else 'N/A'

    # Recherche de l'image dans le bloc "wp-block-image"
    image_div = soup.find('div', class_='wp-block-image')
    image_tag = image_div.find('img') if image_div else None
    image_url = image_tag['src'] if image_tag else 'N/A'

    # Extraction du mode d'emploi (sous forme de liste ordonnée)
    instructions = soup.find('ol')
    instructions_list = []
    if instructions:
        instruction_items = instructions.find_all('li')
        instructions_list = [f"{index + 1}. {item.get_text()}" for index, item in enumerate(instruction_items)]
    else:
        instructions_list = ['N/A']

    # Extraction des conseils d'entraînement (sous forme de liste non ordonnée)
    training_tips_list = soup.find('ul', class_='wp-block-list')
    tips_list = []
    if training_tips_list:
        tips_items = training_tips_list.find_all('li')
        tips_list = [f"{index + 1}. {item.get_text()}" for index, item in enumerate(tips_items)]
    else:
        tips_list = ['N/A']

    return {
        'Title': title,
        'Image URL': image_url,
        'Link URL': url,
        'Description': description,
        'Instructions': " | ".join(instructions_list),
        'Training Tips': " | ".join(tips_list)
    }

# Création du fichier CSV pour enregistrer les données extraites
with open('scraped_exercises.csv', mode='w', newline='', encoding='utf-8') as file:
    writer = csv.DictWriter(file, fieldnames=['Title', 'Image URL', 'Link URL', 'Description', 'Instructions', 'Training Tips', 'Groupe Musculaire'])
    writer.writeheader()

